package ex01_if;

public class Exam02 {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 15;
		
		// 조건은 boolean 값으로 결과값이 나옴
		// 비교 연산자를 사용해서 조건의 결과값을 만들어야 한다.
		if(num1 > num2) {
			System.out.println("num1 이 크다");
		}
		if(num2 > num1) {
			System.out.println("num2 가 크다");
		}
	}
	
}