package ex01_if;

public class Exam01 {
	public static void main(String[] args) {
		// if문 : 조건의 결과값을 이용해서 실행 내용을 다른게 실행
		// 조건의 결과값은 반드시 true 와 false (boolean) 이다.
		// true 면 실행, false 면 실행 하지 않는다.
		
		if (true) {
			System.out.println("참이면 실행.");
		}
		
		if (false) {
			System.out.println("거짓이면 실행 안함.");
		}
	}
}