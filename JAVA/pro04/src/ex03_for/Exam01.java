package ex03_for;

public class Exam01 {
	public static void main(String[] args) {
		// for문 - 반복문 : 반복할 횟수를 알고 있을때 사용
		// 특정 조건에 맞춰서 정해진 횟수를 반복하는 문법
		
		int i; 
		// for 문에서 초기값으로 사용되는 변수는 
		// 반드시 for 문의 식에서 초기화를 해야한다.
		for(i=0;i<3;i++) {
			System.out.println("i 값 : " + i);
		}
		System.out.println("for문 종료 후 i 값 : " + i);
	}
}




