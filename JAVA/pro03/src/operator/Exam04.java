package operator;

public class Exam04 {
	public static void main(String[] args) {
		// 비교 연산자
		// 비교 연산자의 결과값은 true 아니면 false 다.
		
		// == : 같다
		System.out.println(10 == 10);
		System.out.println(10 == 5);
		
		// != : 다르다, 같지 않다
		System.out.println(10 != 5);
		System.out.println(10 != 10);
		
		// > : 크다
		System.out.println(10 > 5);
		System.out.println(10 > 10);
		System.out.println(10 > 15);
	
		// < : 작다
		System.out.println(10 < 5);
		System.out.println(10 < 10);
		System.out.println(10 < 15);
		
		// >= : 크거나 같다
		System.out.println(10 >= 5);
		System.out.println(10 >= 10);
		System.out.println(10 >= 15);
		
		// <= : 작거나 같다
		System.out.println(10 <= 5);
		System.out.println(10 <= 10);
		System.out.println(10 <= 15);
	}
}







